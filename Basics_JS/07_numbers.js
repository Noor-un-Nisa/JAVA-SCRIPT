const marks =new Number(91)// makes sure it is of number data type only
console.log(marks)
// following are the 6 methods of number

console.log(marks.toFixed(2))//fixes the number of values after decimal
console.log(marks.toExponential())// gives the number in exponential form
console.log(marks.toLocaleString())// converts number to string by using locale
console.log(marks.toPrecision(4))//gives specified number off significant digits
console.log(marks.toString())//converts into string datatype
console.log(marks.valueOf())// finds the value of specific variable
